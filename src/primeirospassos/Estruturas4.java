package primeirospassos;

public class Estruturas4 {
	public static void main(String[] args) {
		for (int i = 0; i < 10; i++) {
			System.out.println(i);
		}

		int i = 0;
		while (i < 10) {
			System.out.println(i);
			i++;
		}
		
		int k = 0;
		do {
			System.out.println(k);
			k++;
		} while (k < 10);
	}

}
