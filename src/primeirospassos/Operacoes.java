package primeirospassos;

public class Operacoes {

	public static void main(String[] args) {
		double i = 10;
		double j = 3;
		
		double soma = i + j;
		
		System.out.println("Valor da soma: "+soma);
		
		double subtracao =  i - j;
		System.out.println("Valor da subtração: " + subtracao);
		
		double multiplicacao = i * j;
		System.out.println("Valor da multiplicação:" +multiplicacao);
		
		double divisao = i/j;		System.out.println("Valor da divisão: "+divisao);
		
		double divisao2 = j/i;
		System.out.println("Valor da divisão: "+divisao2);
		
		double mod = i%j;
		System.out.println("Valor do mod: "+mod);
		
	}
	

}
