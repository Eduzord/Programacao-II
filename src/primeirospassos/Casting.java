package primeirospassos;

public class Casting {

	public static void main(String[] args) {
		double d = 10;
		int i = (int) d;
		System.out.println(d);
		System.out.println(i);
		
		long k = 1000000;
		System.out.println(k);
		
		long m = 1000000000000L;
		System.out.println(m);
		
		float f1 = 500.0f;
		
		int j = 5;
		double h = 10;
		
		double v = j + h;
		System.out.println(v);
	}

}
